@javax.persistence.Table(name = "d47tu_finder_types", schema = "", catalog = "testJoomlaDsl")
@Entity
public class D47TuFinderTypesEntity {
    private int id;

    @javax.persistence.Column(name = "id")
    @Id
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    private String title;

    @javax.persistence.Column(name = "title")
    @Basic
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    private String mime;

    @javax.persistence.Column(name = "mime")
    @Basic
    public String getMime() {
        return mime;
    }

    public void setMime(String mime) {
        this.mime = mime;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        D47TuFinderTypesEntity that = (D47TuFinderTypesEntity) o;

        if (id != that.id) return false;
        if (mime != null ? !mime.equals(that.mime) : that.mime != null) return false;
        if (title != null ? !title.equals(that.title) : that.title != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (title != null ? title.hashCode() : 0);
        result = 31 * result + (mime != null ? mime.hashCode() : 0);
        return result;
    }
}
